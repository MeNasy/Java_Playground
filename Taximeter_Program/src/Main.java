import java.util.Scanner;

public class Main
{
	public static void main(String[] args)
	{
		double km, result, startPrice, kmPrice;
		Scanner input = new Scanner(System.in);

		kmPrice = 2.20;
		startPrice = 10;
		System.out.print("Gidilen mesafeyi KM cinsinden giriniz: ");
		km = input.nextDouble();
		result = km * kmPrice + startPrice;

		if (result < 20)
			System.out.println("Ödenecek tutar: 20 TL");
		else
			System.out.println("Ödenecek tutar: "+ result+ " TL");
		input.close();
	}
}
