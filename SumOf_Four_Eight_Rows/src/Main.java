import java.util.Scanner;

public class Main
{
	public static void main(String[] args)
	{
		Scanner input = new Scanner(System.in);
		int	num, sum;

		sum = 0;
		while (true)
		{
			System.out.print("Sayi giriniz: ");
			num = input.nextInt();
			if (num % 2 != 0)
				break;
			else if (num % 2 == 0 && num % 4 == 0)
				sum += num;
		}
		System.out.println("Toplam: " + sum);
		input.close();
	}
}
