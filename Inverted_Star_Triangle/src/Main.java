import java.util.Scanner;

public class Main
{
	public static void main(String[] args)
	{
		Scanner input = new Scanner(System.in);
		int nb,i,j;

		System.out.print("Bir sayi giriniz: ");
		nb = input.nextInt();

		i = nb;
		if (nb > 0)
		{
			while (i > 0)
			{
				j = 0;
				while (j < i)
				{
					System.out.print("*");
					j++;
				}
				System.out.println();
				i--;
			}
		}
		else
			System.err.println("Taban negatif olamaz !");
		input.close();
	}
}
