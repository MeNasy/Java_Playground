import java.util.Scanner;

public class Main
{
	public static void main(String[] args)
	{
		Scanner input = new Scanner(System.in);
		int	num, tmpNum, totalDigit;

		System.out.print("Sayi: ");
		num = input.nextInt();

		if (num < 0)
			num *= -1;
		totalDigit = 0;
		tmpNum = num;
		while (tmpNum != 0)
		{
			totalDigit += tmpNum % 10;
			tmpNum /= 10;
		}
		System.out.println(num + " sayisinin rakamlar toplami: " + totalDigit);
		input.close();
	}
}
