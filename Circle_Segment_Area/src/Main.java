import java.util.Scanner;

public class Main
{
	public static void main(String[] args)
	{
		double rYaricap, merkezAci, pi, result;
		Scanner input = new Scanner(System.in);

		pi = 3.14;
		System.out.print("Yariçapi giriniz (r): ");
		rYaricap = input.nextDouble();
		System.out.print("Merkez açi ölçüsü giriniz: ");
		merkezAci = input.nextDouble();

		result = (pi* (rYaricap* rYaricap) * merkezAci)/360;
		System.out.println("Daire diliminin alani: "+result);
		input.close();
	}
}
